// Helper function to get translation with interpolation
export const t = (key, params = {}) => {
  const keys = key.split('.')
  let value = translations
  
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k]
    } else {
      console.warn(`Translation key not found: ${key}`)
      return key
    }
  }
  
  if (typeof value === 'string') {
    return value.replace(/\{(\w+)\}/g, (match, param) => {
      return params[param] !== undefined ? params[param] : match
    })
  }
  
  return value
}

// Helper function to get translation with pluralization
export const tPlural = (key, count, params = {}) => {
  const translation = t(key, { ...params, count })
  
  if (typeof translation === 'string') {
    return translation.replace(/\{plural\}/g, count === 1 ? '' : 's')
  }
  
  return translation
}

