// Helper function to get Bosnian translation with interpolation
export const tBs = (key, params = {}) => {
  const keys = key.split('.')
  let value = translationsBs
  
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k]
    } else {
      console.warn(`Bosnian translation key not found: ${key}`)
      return key
    }
  }
  
  if (typeof value === 'string') {
    return value.replace(/\{(\w+)\}/g, (match, param) => {
      return params[param] !== undefined ? params[param] : match
    })
  }
  
  return value
}

// Helper function to get Bosnian translation with pluralization
export const tPluralBs = (key, count, params = {}) => {
  const translation = tBs(key, { ...params, count })
  
  if (typeof translation === 'string') {
    return translation.replace(/\{plural\}/g, count === 1 ? '' : 'a')
  }
  
  return translation
}

export default translationsBs
